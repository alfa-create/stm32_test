#include "main.h"

#define SENSOR_ADDR (0x40 << 1)
#define MEASURE_TEMP 0xE3
#define MEASURE_HUMIDITY 0xE5

I2C_HandleTypeDef hi2c1;
DMA_HandleTypeDef hdma_i2c1_rx;
DMA_HandleTypeDef hdma_i2c1_tx;

UART_HandleTypeDef huart2;
